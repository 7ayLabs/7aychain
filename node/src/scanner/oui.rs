pub fn lookup_vendor(oui: &[u8; 3]) -> Option<&'static str> {
    OUI_DATABASE.get(oui).map(|(vendor, _)| *vendor)
}

pub fn lookup_device_type(oui: &[u8; 3]) -> DetectedDeviceType {
    OUI_DATABASE
        .get(oui)
        .map(|(_, device_type)| *device_type)
        .unwrap_or(DetectedDeviceType::Unknown)
}

pub fn infer_device_type_from_name(name: &str) -> DetectedDeviceType {
    let name_lower = name.to_lowercase();

    if name_lower.contains("iphone") {
        DetectedDeviceType::IPhone
    } else if name_lower.contains("ipad") {
        DetectedDeviceType::IPad
    } else if name_lower.contains("macbook") || name_lower.contains("mac book") {
        DetectedDeviceType::MacBook
    } else if name_lower.contains("airpods") || name_lower.contains("air pods") {
        DetectedDeviceType::AirPods
    } else if name_lower.contains("apple watch") || name_lower.contains("watch") {
        DetectedDeviceType::AppleWatch
    } else if name_lower.contains("galaxy")
        || name_lower.contains("pixel")
        || name_lower.contains("oneplus")
        || name_lower.contains("huawei")
        || name_lower.contains("oppo")
        || name_lower.contains("vivo")
        || name_lower.contains("redmi")
        || name_lower.contains("poco")
    {
        DetectedDeviceType::Android
    } else if name_lower.contains("xbox") {
        DetectedDeviceType::GameConsole
    } else if name_lower.contains("playstation") || name_lower.contains("ps5") || name_lower.contains("ps4") {
        DetectedDeviceType::GameConsole
    } else if name_lower.contains("tv") || name_lower.contains("roku") || name_lower.contains("chromecast") {
        DetectedDeviceType::SmartTV
    } else if name_lower.contains("echo") || name_lower.contains("alexa") || name_lower.contains("home mini") {
        DetectedDeviceType::IoTDevice
    } else if name_lower.contains("printer") || name_lower.contains("hp ") || name_lower.contains("epson") {
        DetectedDeviceType::Printer
    } else {
        DetectedDeviceType::Unknown
    }
}

pub fn vendor_to_bytes(vendor: &str) -> [u8; 32] {
    let mut arr = [0u8; 32];
    let bytes = vendor.as_bytes();
    let len = bytes.len().min(32);
    arr[..len].copy_from_slice(&bytes[..len]);
    arr
}

pub fn name_to_bytes(name: &str) -> [u8; 64] {
    let mut arr = [0u8; 64];
    let bytes = name.as_bytes();
    let len = bytes.len().min(64);
    arr[..len].copy_from_slice(&bytes[..len]);
    arr
}
